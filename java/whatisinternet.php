      <h3>What is Internet</h3>
	<p class="normal">To tell the history of the Internet, you have to commit to a definition. The one that springs to mind most easily is a network for sharing information, but this definition is far too broad. Under this definition, a history of the Internet would start with the evolution of language, jump between continents to tell the story of tribal drumming, smoke signals, carrier pigeons, messengers, telegraphs, the transatlantic connection, phone lines and so on. (For background reading, check out What is the difference between the Internet and the World Wide Web?)

Similarly, adding the word “computer” to the definition helps eliminate a brief history of selective pigeon breeding, but it also limits the story a little too much. Computers are still a common interface that people use to connect to the Internet, but smartphones, tablets and future devices make for a laundry list definition where the Internet is a network for sharing information using personal computers, smartphones, e-readers and so on.

A better definition of the Internet, therefore, focuses on the method by which the information is being shared, regardless of the technology. And, at its heart, the tale of the Internet is really a tale of protocols, where protocol is loosely defined as “rules of communication”. For example, the early telegraph system was a communication system where the protocol was the rules behind the dots and dashes telegraph operators used to understand the message.

The Federal Networking Council (FNC) recognized the importance of protocols when it released an official definition of the term on October 24, 1995. According to the FNC, the Internet is a global information system that meets the following three criteria:

Is logically linked together by a globally unique address space based on Internet Protocol (IP) or its subsequent extensions/follow-ons
Is able to support communications using the Transmission Control Protocol/Internet Protocol (TCP/IP) suite or its subsequent extensions/follow-ons, and/or other IP-compatible protocols
Provides, uses or makes accessible, either publicly or privately, high-level services layered on the communications and related infrastructure described herein
Hence, to understand the history of the Internet, is to understand the development of its underlying protocols. Using this approach, now that we have a target, we can track the creation of this global information system from its theoretical roots.

