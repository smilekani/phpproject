	  C++ is a general-purpose object-oriented programming (OOP) language, developed by Bjarne Stroustrup, and is an extension of the C language. 
	  It is therefore possible to code C++ in a "C style" or "object-oriented style." In certain scenarios, it can be coded in either way and
	   is thus an effective example of a hybrid language.

 		C++ is considered to be an intermediate-level language, as it encapsulates both high- and low-level language features. 
 		Initially, the language was called "C with classes" as it had all the properties of the C language with an additional concept of "classes."
 		 However, it was renamed C++ in 1983.

		It is pronounced "see-plus-plus."
		
		C++ is one of the most popular languages primarily utilized with system/application software, drivers, client-server applications and embedded firmware.
		
		The main highlight of C++ is a collection of predefined classes, which are data types that can be instantiated multiple times. 
		The language also facilitates declaration of user-defined classes. Classes can further accommodate member functions to implement specific functionality.
		 Multiple objects of a particular class can be defined to implement the functions within the class. Objects can be defined as instances created at run time. 
		 These classes can also be inherited by other new classes which take in the public and protected functionalities by default.
		 
		 C++ includes several operators such as comparison, arithmetic, bit manipulation and logical operators. 
		 One of the most attractive features of C++ is that it enables the overloading of certain operators such as addition.

		A few of the essential concepts within the C++ programming language include polymorphism, virtual and friend functions, templates, namespaces and pointers.
		
